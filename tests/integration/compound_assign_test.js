// 复合赋值运算符测试

// 测试 += 运算符
let sum = 0;
for (let i = 0; i < 10; i++) {
  sum += i;
}
console.log(sum); // 应该输出 45

// 测试 -= 运算符
let a = 10;
a -= 3;
console.log(a); // 应该输出 7

// 测试 *= 运算符
let b = 5;
b *= 4;
console.log(b); // 应该输出 20

// 测试 /= 运算符
let c = 20;
c /= 4;
console.log(c); // 应该输出 5

// 测试 &= 运算符
let d = 5;  // 二进制: 101
d &= 3;     // 二进制: 011
console.log(d); // 应该输出 1

// 测试 |= 运算符
let e = 5;  // 二进制: 101
e |= 2;     // 二进制: 010
console.log(e); // 应该输出 7

// 测试 ^= 运算符
let f = 5;  // 二进制: 101
f ^= 3;     // 二进制: 011
console.log(f); // 应该输出 6

// 测试 <<= 运算符
let g = 8;   // 二进制: 1000
g <<= 2;     // 左移2位: 100000 = 32
console.log(g); // 应该输出 32

// 测试 >>= 运算符
let h = 8;   // 二进制: 1000
h >>= 2;     // 右移2位: 10 = 2
console.log(h); // 应该输出 2
